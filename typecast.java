public class typecast{
	public static void main(String[] args){
		String name="123456789";
		
		System.out.println("String to Integer : "+Integer.parseInt(name));
		System.out.println("Character Max value : "+Character.MAX_VALUE);
		System.out.println("Integer Max value : "+Integer.MAX_VALUE);
		System.out.println("Double Max value : "+Double.MAX_VALUE);
		System.out.println("Float Max value : "+Float.MAX_VALUE);
		
		System.out.println("\n");
		
		System.out.println("Character Max value : "+Character.SIZE);
		System.out.println("Integer Max value : "+Integer.SIZE);
		System.out.println("Double Max value : "+Double.SIZE);
		System.out.println("Float Max value : "+Float.SIZE);
		
		System.out.println("\n");
		
		System.out.println("Character Max value : "+Character.TYPE);
		System.out.println("Integer Max value : "+Integer.TYPE);
		System.out.println("Double Max value : "+Double.TYPE);
		System.out.println("Float Max value : "+Float.TYPE);
		
	}
}